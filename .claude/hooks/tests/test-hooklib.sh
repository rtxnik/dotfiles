# --- F3: wf_log_skip_debounced optional reason (engine-fallback audit) ---
dbt=$(mktemp -d)
TMPDIR="$dbt" WORKFLOW_AUDIT_PATH="$dbt/a.jsonl" wf_log_skip_debounced f3-test-id engine_fallback_builtin
ok "skip-debounce custom reason recorded" '[ "$(tail -n1 "$dbt/a.jsonl" | jq -r .reason)" = "engine_fallback_builtin" ]'
TMPDIR="$dbt" WORKFLOW_AUDIT_PATH="$dbt/a.jsonl" wf_log_skip_debounced f3-test-id engine_fallback_builtin
