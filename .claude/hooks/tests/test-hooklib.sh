# --- F3 / D5-1: hybrid secret engine (gitleaks layer over the builtin floor) ---
AGE_TKN="AGE-SECRET-KEY-1$(printf 'Q%.0s' $(seq 1 58))"   # assembled; default ruleset class the floor misses
GHP_TKN="ghp_""ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghij"
flr=$(printf 'T=%s\n' "$GHP_TKN" | WORKFLOW_SECRETS_GITLEAKS=off secret_matches_blocking)
ok "blocking tier keeps builtin floor with engine off" '[ -n "$flr" ]'
if command -v gitleaks >/dev/null 2>&1; then
  glo=$(printf 'k=%s\n' "$AGE_TKN" | secret_matches_gitleaks)
  ok "gitleaks engine finds age-key class" '[ -n "$glo" ]'
  ok "gitleaks engine respects off flag (rc1)" '! ( printf "x" | WORKFLOW_SECRETS_GITLEAKS=off secret_matches_gitleaks )'
  blk=$(printf 'k=%s\n' "$AGE_TKN" | secret_matches_blocking)
  ok "blocking tier includes gitleaks layer" '[ -n "$blk" ]'
  un=$(printf 'k=%s\n' "$AGE_TKN" | secret_matches)
  ok "secret_matches union includes gitleaks layer" '[ -n "$un" ]'
  cln=$(printf 'plain text only\n' | secret_matches_blocking)
  ok "blocking tier clean input stays clean" '[ -z "$cln" ]'

  # Error-path: a stub gitleaks that exits rc=1 with empty stdout (config/runtime error)
  # must cause secret_matches_blocking to write an engine_fallback_builtin audit line.
  # Use a fresh TMPDIR so the debounce marker for secret-engine-gitleaks is not already set.
  _err_dir=$(mktemp -d)
  cat > "$_err_dir/gitleaks" <<'STUB'
#!/bin/sh
exit 1
STUB
  chmod +x "$_err_dir/gitleaks"
  _err_audit="$_err_dir/audit.jsonl"
  # Run the blocking tier in a subshell so env overrides are isolated; check the
  # audit file in the outer shell after the subshell exits (the file is on-disk).
  ( export TMPDIR="$_err_dir"; export WORKFLOW_AUDIT_PATH="$_err_audit"
    export PATH="$_err_dir:$PATH"
    printf "plain\n" | secret_matches_blocking >/dev/null 2>&1 )
  ok "engine config/runtime error (rc=1) triggers fallback audit line" \
    '[ -s "$_err_audit" ] && grep -q "engine_fallback_builtin" "$_err_audit"'
  rm -rf "$_err_dir"
else
  echo "SKIP: gitleaks not on PATH — engine-layer cases skipped (floor case above still ran)"
fi

