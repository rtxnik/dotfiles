# --- F4 / D4-2: resolve_gate_status defensive pass|fail|unknown over tool_response shapes ---
gs() { printf '%s' "$1" | resolve_gate_status | cut -f1; }    # status only
gx() { printf '%s' "$1" | resolve_gate_status | cut -f3; }    # src only
ok "status numeric exit 0 -> pass"   '[ "$(gs "{\"exit_code\":0}")" = "pass" ]'
ok "status numeric exit 1 -> fail"   '[ "$(gs "{\"exit_code\":1}")" = "fail" ]'
ok "status exitCode alias -> fail"   '[ "$(gs "{\"exitCode\":2}")" = "fail" ]'
ok "status success:true -> pass"     '[ "$(gs "{\"success\":true}")" = "pass" ]'
ok "status is_error:true -> fail"    '[ "$(gs "{\"is_error\":true}")" = "fail" ]'
ok "status interrupted -> fail"      '[ "$(gs "{\"interrupted\":true}")" = "fail" ]'
ok "status stdout marker -> pass"    '[ "$(gs "{\"stdout\":\"Results: 12 passed, 0 failed\"}")" = "pass" ]'
ok "status stderr marker -> fail"    '[ "$(gs "{\"stderr\":\"FAILED: boom\"}")" = "fail" ]'
