-- =============================================================================
-- Keymaps
-- =============================================================================

local map = vim.keymap.set

-- -----------------------------------------------------------------------------
-- Completion toggle
-- -----------------------------------------------------------------------------

map("n", "<leader>p", '<cmd>lua require("cmp").setup { enabled = true }<cr>', { desc = "Enable completion" })
map("n", "<leader>P", '<cmd>lua require("cmp").setup { enabled = false }<cr>', { desc = "Disable completion" })

-- -----------------------------------------------------------------------------
-- LSP
-- -----------------------------------------------------------------------------

map("n", "<leader>S", "<cmd>LspStop<CR>", { desc = "Stop LSP" })

-- -----------------------------------------------------------------------------
-- Navigation (keep cursor centered)
-- -----------------------------------------------------------------------------

map("n", "<C-d>", "<C-d>zz")
map("n", "<C-u>", "<C-u>zz")
map("n", "n", "nzzzv")
map("n", "N", "Nzzzv")

-- -----------------------------------------------------------------------------
-- Telescope
-- -----------------------------------------------------------------------------

map("n", "<leader>fs", "<cmd>Telescope symbols<cr>", { desc = "Find symbols" })

-- -----------------------------------------------------------------------------
-- Text manipulation
-- -----------------------------------------------------------------------------

map("n", "<leader>wsq", 'ciw""<Esc>P', { desc = "Surround word with quotes" })

-- -----------------------------------------------------------------------------
-- Zettelkasten
-- -----------------------------------------------------------------------------

map("n", "<leader>zp", "ggi---<CR>publish: true<CR>---<CR><CR><Esc>", { desc = "Add publish frontmatter" })
